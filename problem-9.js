// ৯. পজ টি , বনজগ্টি  ো জ জরা বেি িরা
// একটি সংখ্যা ইনপুি নাও। যদি সংখ্যা পক্ত টিভ হয়, "Positive" দিন্ট কজরা। বনজগটিভ হজে
// "Negative" দিন্ট কজরা। িূনয হজে "Zero" দিন্ট কজরা।

var number;

number = -5;

if (number > 0) {
  console.log("Positive");
} else if (number === 0) {
  console.log("Zero");
} else {
  console.log("Negative");
}
